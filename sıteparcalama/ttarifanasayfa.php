					<p id="sbif"><h1>Soğuk baklava</h1>
					<br>
					Malzemeler:
					<br>
					1 paket baklavalık yufka (800g)
					<br>
150g tereyağı
<br>
3/4 (bir yarım+bir çeyrek) çay bardağı sıvı yağ (1 çay bardağından biraz az)
<br>
<br>
Şerbeti için;
<br>
2 su bardağı şeker
<br>
1 su bardağı su
<br>
2 su bardağı süt
<br>
<br>
Arası için;
<br>
1 kase iri çekilmiş fıstık (dilerseniz fındık ya da ceviz de kullanabilirsiniz)
<br>
<br>
Üzeri için;
<br>
2 yemek kaşığı kakao
<br>
1 yemek kaşığı süt tozu
<br>
<br>
Nasıl yapılır:
<br>

Tatlımızın ilk olarak şerbetini hazırlayalım. Bunun için tenceremize şeker ve suyu alarak kaynamaya bırakalım. Kaynadıktan 5 dakika sonra ocaktan alalım ve soğumaya bırakalım.
Ardından küçük bir sos tavasına ya da küçük bir tencereye tereyağımızı alalım ve eritelim. Yağımız erirken üzerinde oluşan köpükleri bir kaşık yardımı ile alalım.
Yağımız eridikten sonra ocaktan alalım ve üzerine sıvı yağımızı ekleyerek karıştıralım.
Baklava yufkamızı tezgaha alalım, kullanacağımız fırın kabımızı yufkamızın kenar kısmına yerleştirerek etrafından bıçak ile keselim. Ben bu tarifte küçük boy yuvarlak borcam kullandım (27cm).
Baklavamın çok kalın olmasını istemediğim için kenarda kalan baklava yufkasını kestim ve daha sonra başka bir tarifte kullanmak için hava almayacak şekilde paketleyerek kaldırdım (fotoğraflarda ve videoda daha net olarak görebilirsiniz)
Ardından daire şeklindeki yufkaların kenarlarında kalan kısımları da aralara yerleştirmek aldım. Bu aşamada baklava yufkalarınızın kurumaması için yufkalarınızı temiz bir örtü arasında tutmalısınız.
Daire şeklindeki yufkalarımızı alt ve üstte kullanmak için eşit olacak şekilde ayıralım.
Fırın tepsimizin tabanına yağ karışımımızdan 1 kaşık kadar alalım ve fırça ile her yerine gelecek şekilde sürelim.
Daha sonra 1 tane bütün yufkamızdan yerleştirelim.  Üzerine kenarlarda kalan parçalardan da bir kaç yaprak yerleştirelim. Ve 1 kaşık daha yağ gezdirelim.
Tekrar daire şeklindeki yufkamızdan yerleştirelim. Üzerine parçalı yufkalardan yerleştirerek üzerine bir kaşık yağ gezdirelim.
Bu şekilde yufkaların yarısını tepsimize üst üste sıralayalım (Son 2 katta bütün yufka parçalarından yerleştirelim.)
Ardından baklavamızın ara malzemesini yerleştirelim. Ben iri çekilmiş fıstık kullandım siz dilerseniz fındık ya da cevizde kullanabilirsiniz.
 Bu aşamada fırınımızı alt üst fansız ayarda 170°C’de ısınmaya bırakalım.
Fıstıklarımızı yerleştirdikten sonra üst için ayırdığımız yufkalarımızı üst üste, aralarına yağ dökerek aynı şekilde sıralamaya devam edelim.
Yufkaları yerleştirme işlemi bittikten sonra kenardan taşan kısımlar varsa bıçak ile keselim ve yağımızdan sürelim.
Hazırladığımız baklavamızı dilediğimiz boyutta keselim.
Baklavamızı 170°C fırında altı ve üstü nar gibi kızarana kadar pişmeye bırakalım. Ben 1 saat 10 dakika kadar pişirdim, fırına göre değişir bu süre, acele etmeden güzelce pişmesini bekleyin.
Baklavamız pişerken biz de şerbetimizin sütünü ekleyelim ve karıştırarak bekletelim.
Kızaran baklavamızı fırından alalım ve ilk sıcağının çıkması için 10 dakika kadar bekletelim.
Soğuyan baklavamızın üzerine şerbeti her yerine gelecek şekilde gezdirelim. Şerbeti çekmesi için 1 saat kadar dinlendirelim.
Sıra geldi baklavamızın üzerini süslemeye. Ben kakaoyu süt tozu ile karıştırarak açtım. Ardından süzgeç yardımı ile baklavanın her yerini bu karışımla kapladım. Buzdolabında dinlendirdiğimiz soğuk baklavamız hazır.
Üzerini toz fıstıkla ve çikolata rendesi ile süsleyerek servis edebilirsiniz.
