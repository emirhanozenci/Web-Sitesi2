<p id="cmbif"><h1>Çilekli magnolya</h1>
<br>
Malzemeler:
<br>
2 paket Eti Burçak bisküvi
<br>
10-15 adet çilek
<br>
<br>
Muhallebisi İçin;
<br>
1 litre süt
<br>
1 su bardağı toz şeker
<br>
2 adet yumurta sarısı
<br>
1 Türk kahvesi fincanı mısır nişastası
<br>
2 yemek kaşığı tepeleme un
<br>
1 adet vanilya
<br>
1 kutu krema (200 ml)
<br>
<br>
Nasıl Yapılır:
<br>

İlk iş olarak, 2 paket Eti Burçak bisküviyi rondoda çekin toz haline getirin.
Çilekleri yeşil yaprak bölümlerini koparmadan su dolu bir kasede bekletin. Temizlensinler.
Krema, bisküvi ve çilek hariç, muhallebi malzemesi ile muhallebinizi hazırlayın. Yani süt, toz şeker, yumurta sarısı, mısır nişastası, un ve vanilyayı derin bir tencerede güzelce telle çırpın ve ocakta muhallebi kıvamı alana kadar göz göz olup, pıt pıt atana kadar pişirin.
Şimdi suda bekleyen çilekleri sudan üstten toplayarak elinizle nazikçe alın, süzgece koyun ve çileklerin yaprak kısımlarını bir bıçak ile alın, atın. Çilekler süzgeçte beklesinler.
Muhallebiniz bu arada pişti, şimdi muhallebi soğusun.
Siz bu arada süzülmüş çilekleri dilimleyin.
Magnoliayı hazırlayacağınız kupları, derin kaseleri ya da benim gibi geniş derin bardaklarınızı  hazırlayın.
Muhallebi soğudu, şimdi içine 1 kutu kremayı ekleyin ve telle güzelce karıştırın, homojen bir karışım olsun.
Şimdi hepsini tüm malzemeyi birleştirme zamanı.
Bardakların en alt bölümüne, yani dibine çektiğimiz bisküvilerden 2 yemek kaşığı koyun.
Şimdi bardağın kenarlarına dilimlenmiş çilekleri güzelce yerleştirin, dizin.
Bardağınıza şimdi pişmiş muhallebiden 1’er kepçe koyun.
Üzerine tekrar 1 kaşık bisküvi ve tekrar puding koyun. Bu şekilde pudingi paylaştırıp, bitirin.
Tatlının en üstünü de kalan bisküvilerinizi paylaştırarak kapatın. Tatlınızı bitirin.
En üstünü çilek dilimleri ile süsleyebilirsiniz.
Lütfen tatlınızı buzdolabında 4-5 saat soğutup, soğuk soğuk servis yapın. Ayrıca tatlınızı hazırladığınız gün içinde tüketmenizi öneririm. Bir gün sonraya kalınca, şahsi fikrim tatlının ilk günkü lezzetinde olmadığı yönünde. İlk gün bisküviler kıtır kıtır, meyveler çıtır çıtır oluyor. Buzdolabında beklemek bisküvileri  nemlendiriyor, meyveler de çıtırlıklarını kaybediyor. Yine de tercih sizin.
