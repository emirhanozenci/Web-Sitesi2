<p id="dcbif"><h1>Domates çorbası</h1>
<br>
Malzemeler:
<br>
4-5 adet olgun domates
<br>
2 çorba kaşığı tereyağı
<br>
3 çorba kaşığı un
<br>
1 litre sıcak su
<br>
1 su bardağı süt
<br>
1 çorba kaşığı rendelenmiş taze kaşar
<br>
<br>
Nasıl Yapılır:
<br>

Domates çorbası yapmak için yağ ve un bir tencerede hafifçe kavrulur.
Diğer taraftan kabuğu çıkarılıp, küp küp kesilmiş domates robottan geçirilip bu karışıma ilave edilir, birkaç dakika kavrulur.
Ara verilmeden bir litre kadar su ilave edilip karıştırma işlemini sürdürülür. 15 dakika bu şekilde kaynatılır.
Daha sonra süt ilave edilip birkaç dakika daha kaynatılarak tuzu ilave edilip ocaktan alınır.
Arzuya göre servis yaparken üzerine kaşar peyniri rendesi ilave edilir. Domates çorbamız servise hazır.
