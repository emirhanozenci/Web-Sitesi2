<p id="kpbif"><h1>Kelle paça çorbası</h1>
<br>
Malzemeler:
<br>
1 kilo kelle eti (düdüklüde haşlanıp ayıklanmış)
<br>
2 yumurta sarısı
<br>
1 yemek kaşığı tepeleme un
<br>
4 yemek kaşığı yoğurt
<br>
2 diş sarımsak
<br>
Yarım limon suyu
<br>
1 yemek kaşığı tereyağ
<br>
1, 5 veya 2 litre su (yoğun bir tadı olsun isterseniz haşlama suyundan da kullanabilirsiniz)
<br>
<br>
Nasıl Yapılır:
<br>

Unu tereyağında kavurarak çorbanın yapımına başlıyoruz.
Çırpma teli yardımıyla suyunu azar azar ilave edip kaynamaya bırakıyoruz.
Bir kasenin içinde yumurta sarısı, limon suyu ve yoğurdu çırpıyoruz.
Kaynayan sudan da bir kepçe ilave edip tencereye azar azar ve çırparak döküyoruz.
Haşlanıp ayıklanmış kelle etini ve ezilmiş sarımsağı ilave edip bir iki taşım kaynatıyoruz,
son olarak tuzunu ilave ediyoruz. Ve lokantada içtiğiniz kelle paçayı aratmayacak çorbanız hazır.
