<p id="secbif"><h1>Şehriye çorbası</h1>
<br>
Malzememler:
<br>
1 litre sıcak su
<br>
1 çay bardağı tel şehriye
<br>
2 yemek kaşığı tereyağı
<br>
1 su bardağı domates rendesi
<br>
Karabiber, pul biber,Tuz.
<br>
<br>
Nasıl yapılır:
<br>

Şehriye çorbası için ilk olarak uygun bir tencerede tereyağını alıp eritelim.
Tereyağı eridikten sonra üzerine domates rendesini ilave ederek kavuralım.
Üzerine sıcak suyu ekleyerek kaynamaya bırakalım.
Kaynayan suyumuza tel şehriyeleri ekleyip karıştıralım.
Son olarak karabiber, pul biber ve yeteri kadar tuzu ilave edip şehriyeler yumuşayıncaya kadar pişirelim.
Çorbamız piştikten sonra sıcak olarak servis edelim.
