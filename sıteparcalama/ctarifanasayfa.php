<p id="sebbif"><h1>Sebzeli çorba</h1>
<br>
Malzemeler:
<br>
2 adet kabak
<br>
3 adet havuç
<br>
3 adet patates
<br>
2-3 adet defne yaprağı
<br>
2 su bardağı süt
<br>
2 yemek kaşığı tereyağı
<br>
Yarım çay bardağı sıvı yağ
<br>
1 yemek kaşığı un
<br>
2 tatlı kaşığı tuz
<br>
1 tatlı kaşığı kırmızı toz biber
<br>
1 çay kaşığı karabiber
<br>
2,5 litre sıcak su
<br>
1 avuç dereotu (isteğe bağlı)
<br>
<br>
Nasıl Yapılır:
<br>

Yıkayıp kabuklarını soyduğumuz kabak, havuç ve patatesleri küp küp doğrayalım ve defne yapraklarıyla beraber tencereye alalım. Bu tarifte önceden kabak dolması yapmak için oyduğunuz kabak içleriniz varsa onları da kullanabilirsiniz.
Sebzelerin üzerine sıcak su ilave edelim ve yumuşayıncaya kadar pişmeye bırakalım.
Ayrı bir tavada, tereyağı ve sıvı yağı eritelim.
Üzerine un ekleyerek, birkaç dakika kavuralım.
Karabiber ve kırmızı toz biberi de ekleyelim ve 1-2 dakika daha kavuralım, ateşten alalım.
Tencereden defne yapraklarını alalım ve yumuşayan sebzeleri blenderdan geçirelim.
Un ve baharatla kavurduğumuz yağı tencereye ekleyelim. Bu şekilde bir taşım daha kaynatalım.
Son aşamada, çorbaya süt ilavesini yapalım ve birkaç dakika daha pişirelim.
Tuzunu ekleyip, karıştıralım ve ocağı kapatalım.
İsteğe bağlı olarak, çorbamız piştikten sonra içerisine ince kıyılmış dereotu ilavesi de yapabilirsiniz.
