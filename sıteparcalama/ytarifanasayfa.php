					<p id="mbif"><h1>Mantar soslu biftek</h1></p>

					<br>
Malzemeler:<br>					
2-3 adet biftek (tanesi yaklaşık 200 gram)<br>
Zeytinyağı<br>
Tuz<br>
Karabiber<br>
<br>
Sos için:<br>

350 gram mantar<br>
