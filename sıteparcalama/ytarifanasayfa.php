1 kilogram taze bakla
<br>
1 adet orta boy soğan
<br>
1 yemek kaşığı salça
<br>
Pul biber
<br>
Karabiber
<br>
1 yemek kaşığı tepeleme un
<br>
Yarım limon
<br>
1 silme yemek kaşığı şeker
<br>
Sıvı yağ
<br>
<br>
Nasıl yapılır:<br>

Taze baklaların üzerine gelecek kadar su, bir kabın içerisine doldurulur. Un ve limon suyu ilave edilerek, çırpılır.
Bakla temizlenerek, küçük küçük doğranır.
Bol suda yıkanır ve unlu limonlu suyun içerisinde baklalar kararmasın diye bekletilir. Taze bakla yemeği tarifinin en önemli püf noktası budur.
Bu arada, tencerede soğanlar pembeleşene kadar sıvı yağda kavrulur.
Salça ve baharatlar ilave edilir.
Baklalar süzülerek, yıkanmadan tencereye ilave edilir.
Birkaç dakika kavrulur.
Kaynamış su, baklaların üzerini 1-2 parmak geçinceye kadar ilave edilir.
Tuz ve şeker eklenerek, düdüklü tencerenin kapağı kapatılır.
Kaynadıktan sonra, kısık ateşte yarım saat kadar pişirilerek ocaktan alınır.
Sarımsaklı yoğurt ile servis yapılması tavsiye edilir.
