<br>
1 yemek kaşığı salça
<br>
1 su bardağı domates püresi
<br>
1 kavanoz garnitür
<br>
1 tatlı kaşığı tuz
<br>
Yarım çay kaşığı karabiber
<br>
1 çay kaşığı kırmızı toz biber
<br>
1 su bardağı sıcak su
<br>
<br>
Beşamel Sosu İçin;
<br>
3 yemek kaşığı tereyağı
<br>
3 yemek kaşığı un
<br>
3 su bardağı süt
<br>
Tuz
<br>
Karabiber
<br>
<br>
Üzeri İçin;
<br>
Kaşar peyniri
<br>
<br>
Nasıl Yapılır:
<br>

Uygun bir tencereyi ocağa alalım ve içerisine sıvı yağı ekleyelim.
Soğanımızı yemeklik doğrayalım, tenceremize alalım ve pembeleşene kadar kavuralım.
Daha sonra etimizi kuşbaşı doğrayalım, tenceremize ekleyip rengi dönene kadar kavuralım.
Üzerine doğradığımız kırmızı biberi ve salçayı ekleyerek karıştıralım.
Domates püresini ilave edelim. Garnitürün suyunu süzelim ve onu da ekleyelim.
Daha sonra tuz, karabiber, kırmızı toz biber ve sıcak suyu ekleyelim, 5 dakika bu şekilde pişirip ocaktan alalım.
Beşamel sosu için; tencereye tereyağımızı alalım ve eritelim.
Üzerine unu ekleyerek, birkaç dakika kavuralım.
Kavurduğumuz unun üzerine, sütü yavaş yavaş ekleyelim ve bir yandan da karıştıralım.
Kaynayıp koyulaşan beşamel sosumuzu ocaktan alalım. Tuzunu ve karabiberini ekleyerek karıştıralım.
Pişirdiğimiz tavuğumuzu fırın kabına alalım, üzerine beşamel sosu dökelim ve düzeltelim.
Yemeğimizin üzerini bolca kaşar peyniri rendesi ile kaplayalım ve 190°C fırında, peynirler eriyip kızarıncaya kadar pişirelim.
Fırından alıp 10 dakika dinlendirdiğimiz yemeğimizi dilimleyelim, servis edelim.
