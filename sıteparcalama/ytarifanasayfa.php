<p id="bkbif"><h1>Tavuklu buğu kebap</h1>
<br>
Malzemeler:
<br>
3-4 yemek kaşığı sıvı yağ
<br>
1 adet soğan
<br>
1 kg kemiksiz tavuk
<br>
