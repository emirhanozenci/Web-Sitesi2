Streç filmle üstü kapalı 20/25 dakika bekletmeniz kafidir. Büyük bir borcam veya fırın tepsisi üzerine karışım dökülür.
Kalın-küp doğradığımız patateslerimiz, ve yuvarlak form kazandırdığımız havuçlarımız da ilave edilerek son bir kez daha harç karıştırılır. Dilerseniz patates ve havuçları sosa katmadan biber ve domateslerle birlikte tepsiye de alabilirsiniz.
Tepsi üzerine dengeli bir şekilde yayıp, yer yer biberlerimiz ve 4 parçaya ayırdığımız çerilerimiz eklenir.
Önceden ısıtılmış 220° fırında ilk 25 dakika kontrollü pişirildikten sonra, derece 190/200 arasına getirilip 35 dakika daha pişirilerek süre tamamlanır.
