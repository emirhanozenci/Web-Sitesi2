Tuz<br>
Karabiber<br>
<br>
 Nasıl Yapılır:<br>

Biftekleri zeytinyağı, tuz ve karabiber ile marine edip buzdolabının dışında en az 30 dakika bekletiniz. Eğer buzdolabından çıkartıp hemen tavaya alırsanız etin içindeki kaslar sertleşir ve böylelikle et de sert bir kıvam alır.

Mantarları ortadan ikiye kesip ince dilimlere doğrayınız. Sıvı yağını tavaya alıp üzerine mantarları koyup yüksek ateşte kızartıp kavurunuz. Sonra ocağı orta ateşe alıp üzerine kremayı döküp karıştırınız. Karabiberini ve tuzunu ekleyiniz. Unu suyla açıp sosa ilave ediniz. Birkaç dakika kadar daha karıştırarak pişirdikten sonra ince kıyılmış maydanozu ekleyiniz.

Biftekleri grill tavasında önlü arkalı kızartınız. Zevkinize göre içi hafif kırmızı kalsın istiyorsanız daha kısa, tam pişmiş seviyorsanız da daha uzun pişiriniz.

Biftekleri tabaklara alıp mantarlı sosu üstüne dökünüz. Yanına patates püresi veya bol bir salata ile servis edebilirsiniz.
