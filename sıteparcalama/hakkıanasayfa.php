			<h3> Gerek servis anlayışı, gerekse muhteşem doğa dokusuyla misafirlerine alternatifsiz bir ortam sunuyor.

1996'dan beri Bursa'da hizmetini sürdüren Life Restaurant, yenilenen bahçesi ve iç mekanıyla kalite ve konforu seçkin müşterileriyle buluşturmaya devam ediyor.

Bahçede sizi etrafı yemyeşil ağaçlarla çevrili harika bir atmosfer karşılıyor. Bahçenin ortasında minik bir havuz ve yerleri gemi güvertesini andıran ahşap döşemeler, keyifli geçirilecek saatler için tamamlayıcı unsurlar olarak yer alıyor.

Yemeklere gelince; menü dünyanın her yerinden bir parça lezzet almış. Meksika'dan Nacholar, Burritolar, Kaliforniya'dan Buffalo Tavuk Kanatları, bir Fransız klasiği olan Restaurantta Paris soslu bonfile, İtalyan tarzı ince hamurlu pizzalar ve muhteşem Cheese Cake'ler.

Yediden yetmişe herkesin ikinci adresi olan Life Restaurant, Uludağ Üniversitesi öğrencileri tarafından 2 kez en çok gidilen Restaurant ödülüne layık görülmüştür.

Life Restaurant; 2. şubesini Ocak 2015 de Mustafakemalpaşa da Podyum park alışveriş merkezinde açtı.

